void PrintMatrix(int[,] matrix) //функци выводит двухмерный массив
{
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            if (matrix[i, j] == 0) System.Console.Write(" ");
            else if(matrix[i, j] == 2) System.Console.Write("+");
            else System.Console.Write("0");
        }
        System.Console.WriteLine("");
    }
}


void FillMatrix(int x, int y)
{
    if (pic[x, y] == 0)
    {
        pic[x, y] = 2;
        FillMatrix(x - 1, y);
        FillMatrix(x, y - 1);
        FillMatrix(x + 1, y);
        FillMatrix(x, y + 1);
    }

}

